package com.mrv.yangtools.common;

/**
 * @author bartosz.michalik@amartus.com
 */
public class Tuple<A, B> {
    private final A first;
    private B second;

    public Tuple(A first, B second) {
        this.first = first;
        this.second = second;
    }

    public A first() {
        return first;
    }

    public B second() {
        return second;
    }

    @Override
    public String toString() {
        return first + "." + second;
    }
}
